#ifndef ALGORITHM_SEARCHING_COMPARABLE_H
#define ALGORITHM_SEARCHING_COMPARABLE_H

#include <concepts>

template <typename T>
concept Comparable = requires(T a, T b) {
    { a < b } -> std::convertible_to<bool>;
    { a > b } -> std::convertible_to<bool>;
    { a <= b } -> std::convertible_to<bool>;
    { a >= b } -> std::convertible_to<bool>;
    { a == b } -> std::convertible_to<bool>;
    { a != b } -> std::convertible_to<bool>;
};

#endif //ALGORITHM_SEARCHING_COMPARABLE_H
